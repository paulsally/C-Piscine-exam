#include <unistd.h>

int	main(void)
{
	char	c;
	char	c_up;

	c = 'A';
	while (c <= 'Z')
	{
		if (c % 2 != 0)
		{
			c_up = c + 32;
			write (1, &c_up, 1);
		}
		else
		{
			write (1, &c, 1);
		}
		c++;
	}
	write (1, "\n", 1);
	return (0);
}
